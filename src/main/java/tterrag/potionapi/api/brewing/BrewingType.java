package tterrag.potionapi.api.brewing;

public enum BrewingType
{
    /**
     * A new potion is being created.
     */
    POTION,

    /**
     * The power level of the potion is being increased.
     */
    POWER_INCREASE,

    /**
     * The time level of the potion is being increased.
     */
    TIME_INCREASE
}
